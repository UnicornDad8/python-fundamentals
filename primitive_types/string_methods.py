# String Methods

course = "Python Programming"

print(course)
print(course.upper())
print(course.lower())
print(course.title())  # capitalize the first letter of each word
print(course.strip())  # removes white space from the begining and end of the string
print(course.find("Pro"))  # finds the index of "Pro"
print(course.replace("P", "A"))
print("Pro" in course)
print("Swift" not in course)
